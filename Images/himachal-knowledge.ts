export function getSmartFallback(query: string): string {
    const lowerQuery = query.toLowerCase();

    // 1. Search for matching district or place
    const match = HIMACHAL_KNOWLEDGE.districts.find(d =>
        d.name.toLowerCase().includes(lowerQuery) ||
        d.keywords.some(k => lowerQuery.includes(k)) ||
        d.popular_places.some(p => lowerQuery.includes(p.toLowerCase()))
    );

    if (match) {
        // Construct a helpful response based on the matched district
        const places = match.popular_places.slice(0, 5).join(', ');
        return `I found some great info about ${match.name}! \n\n📍 **Top Places:** ${places}... and more.\n🏨 **Hotels:** Budget starts at ${match.hotels.budget}, Luxury goes up to ${match.hotels.luxury}.\n✨ **Must Do:** ${match.activities[0]} and try ${match.food[0]}.\n\nWould you like me to plan a trip here?`;
    }

    // 2. Keyword based responses
    if (lowerQuery.includes('hotel') || lowerQuery.includes('stay') || lowerQuery.includes('price')) {
        return "Hotels in Himachal usually range from ₹1200 (Budget) to ₹6000+ (Luxury). I can help you find stays in Manali, Shimla, or Spiti. Which location are you visiting?";
    }

    if (lowerQuery.includes('cab') || lowerQuery.includes('taxi') || lowerQuery.includes('car')) {
        return "We have Sedans, SUVs, and Tempo Travellers available. You can use the 'Fare Estimator' on this page to check rates!";
    }

    // 3. Generic Fallback
    return "I am currently running in offline mode. I can tell you about Manali, Shimla, Spiti, Dalhousie and more. Try asking 'Tell me about Kasol' or 'Hotels in Shimla'.";
}
